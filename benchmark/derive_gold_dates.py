#!/usr/bin/env python3
"""Derive per-task build dates from gold DuckDB files.

Non-discriminate algorithm applied identically to all tasks:
  1. Find tables matching spine/calendar/dim_date keywords
  2. Look for a `date_day` column (the canonical dbt date-spine grain)
  3. Take max(date_day) + 1 across all matches  →  this is the build date
  4. Fallback: today's date (tasks without date spines don't need FAKETIME)

Produces benchmark/gold_build_dates.json mapping instance_id -> YYYY-MM-DD.
The dbt wrapper in Dockerfile.dbt-agent applies this as FAKETIME for run/compile/build.
"""

from __future__ import annotations

import json
import sys
from datetime import date, datetime, timedelta
from pathlib import Path

# Default gold directory — resolved relative to this script's location.
# Override via CLI: python derive_gold_dates.py /path/to/gold
_SCRIPT_DIR = Path(__file__).resolve().parent
GOLD_DIR = _SCRIPT_DIR.parent / "spider2-repo" / "spider2-dbt" / "evaluation_suite" / "gold"
OUTPUT = _SCRIPT_DIR / "gold_build_dates.json"

# Table name keywords that indicate a date spine / calendar table.
# These tables are generated by dbt up to current_date, so their max date
# reveals when the gold was built.
SPINE_TABLE_KEYWORDS = (
    "calendar",
    "spine",
    "dim_date",
    "dim_dates",
    "dim_time",
    "date_dim",
    "all_dates",
    "metricflow_time_spine",
    "util_months",
)

# Minimum plausible build date — spines ending before this are stale data,
# not build-date indicators (e.g. zuora transaction spine ending in 2016).
MIN_PLAUSIBLE = date(2018, 1, 1)


def derive_date_for_task(task_dir: Path, today: date) -> tuple[str, str]:
    """Return (YYYY-MM-DD, reason) for a single task."""
    import duckdb

    dbs = list(task_dir.glob("*.duckdb"))
    if not dbs:
        return today.isoformat(), "no duckdb file"

    try:
        con = duckdb.connect(str(dbs[0]), read_only=True)
    except Exception as e:
        return today.isoformat(), f"cannot open: {e}"

    try:
        tables = [r[0] for r in con.execute("SHOW TABLES").fetchall()]
    except Exception:
        con.close()
        return today.isoformat(), "cannot list tables"

    candidates: list[tuple[date, str]] = []  # (max_date, "table.column")

    for table in tables:
        # Only look at spine/calendar/dim_date tables
        table_lower = table.lower()
        if not any(kw in table_lower for kw in SPINE_TABLE_KEYWORDS):
            continue

        try:
            cols = con.execute(f'DESCRIBE "{table}"').fetchall()
        except Exception:
            continue

        for col_name, col_type, *_ in cols:
            # Only the canonical `date_day` column — the standard dbt spine grain.
            # This avoids picking up projected columns (same_day_next_year, etc.)
            # or non-spine date columns (period_last_day, month_end_date, etc.)
            if col_name.lower() != "date_day":
                continue

            try:
                row = con.execute(
                    f'SELECT MAX("{col_name}")::DATE FROM "{table}" '
                    f'WHERE "{col_name}" IS NOT NULL'
                ).fetchone()
                if row and row[0]:
                    val = row[0]
                    if isinstance(val, datetime):
                        val = val.date()
                    if val >= MIN_PLAUSIBLE:
                        candidates.append((val, f"{table}.{col_name}"))
            except Exception:
                continue

    con.close()

    if not candidates:
        return today.isoformat(), "no date_day spine found"

    # Take the latest date_day across all spine tables, +1 day = build date
    # Cap at today — future spine dates are pre-generated projections, not build dates
    best_date, best_src = max(candidates, key=lambda x: x[0])
    build_date = min(best_date + timedelta(days=1), today)
    return build_date.isoformat(), f"{best_src} max={best_date}"


def main() -> None:
    gold_dir = GOLD_DIR
    if len(sys.argv) > 1:
        arg = Path(sys.argv[1])
        if arg.is_dir():
            gold_dir = arg

    if not gold_dir.exists():
        print(f"Error: gold directory not found: {gold_dir}")
        print("Usage: python derive_gold_dates.py [/path/to/gold/]")
        sys.exit(1)

    today = date.today()
    task_dirs = sorted(d for d in gold_dir.iterdir() if d.is_dir() and list(d.glob("*.duckdb")))

    print(f"Scanning {len(task_dirs)} gold databases in {gold_dir}")
    print(f"Fallback date (today): {today}")
    print()

    results: dict[str, str] = {}
    for task_dir in task_dirs:
        iid = task_dir.name
        build_date, reason = derive_date_for_task(task_dir, today)
        results[iid] = build_date
        marker = "*" if "date_day" in reason else " "
        print(f"  {marker} {iid:45s} {build_date}  ({reason})")

    with open(OUTPUT, "w") as f:
        json.dump(results, f, indent=2, sort_keys=True)
        f.write("\n")

    derived = sum(1 for r in results.values() if r != today.isoformat())
    print(f"\nWrote {OUTPUT}")
    print(f"  {derived}/{len(results)} derived from spines, "
          f"{len(results) - derived} fallback to {today}")


if __name__ == "__main__":
    main()
